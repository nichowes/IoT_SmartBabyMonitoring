class BABY_ANALYSIS:
    def __init__(self, id, babyName, babyAge, babyHeight, babyWeight,
                 averageNumberOfWakeupsPerDay, averageWakeupTimesPerDay, averageNumberOfFeedsPerDay,
                 averageFeedTimesPerDay, favouriteDayTimeCalmDownMethod, favouriteNightTimeCalmDownMethod,
                 preferredTemperature):
        self.id = id
        self.babyName = babyName
        self.babyAge = babyAge
        self.babyHeight = babyHeight
        self.babyWeight = babyWeight
        self.averageNumberOfWakeupsPerDay = averageNumberOfWakeupsPerDay
        self.averageWakeupTimesPerDay = averageWakeupTimesPerDay
        self.averageNumberOfFeedsPerDay = averageNumberOfFeedsPerDay
        self.averageFeedTimesPerDay = averageFeedTimesPerDay
        self.favouriteDayTimeCalmDownMethod = favouriteDayTimeCalmDownMethod
        self.favouriteNightTimeCalmDownMethod = favouriteNightTimeCalmDownMethod
        self.preferredTemperature = preferredTemperature
